package com.youanmi.bi.demo.es;

/**
 * @author sunxiaolong
 */
public class Log {
    /**
     * 日志的时间
     */
    private Long time;
    /**
     * 日志的内容
     */
    private String content;

    public Long getTime() {
        return time;
    }

    public void setTime(Long time) {
        this.time = time;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

}
